#include<stdio.h>

// division operator(/)
/*  int/int = int 
    int/float = float
    float/int = float
    float/float = float */

    void main()
    {
        printf("%d \n",5/2);
        printf("%d \n",5.0/2);
        printf("%d \n",5/2.0);
        printf("%d \n\n",5.0/2.0);

        printf("%f \n",5/2);
        printf("%f \n",5.0/2);
        printf("%f \n",5/2.0);
        printf("%f \n",5.0/2.0);
    }